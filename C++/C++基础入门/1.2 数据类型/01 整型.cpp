#include<iostream>
using namespace std;

int main()
{
	//整型
	//1、短整型 (-32769~32767)
	short num1 = 32768;

	//2、整型
	int num2 = 327668;

	//3、长整型
	long num3 = 10;

	//4、长长整型
	long long num4 = 10;

	cout << "num1 = " << num1 << endl;
	cout << "num2 = " << num2 << endl;
	cout << "num3 = " << num3 << endl;
	cout << "num4 = " << num4 << endl;

	system("pause");

	return 0;
}
